class String extends NamedTag{

	public function getType(){
		return NBT_new::TAG_String;
	}

	public function read(NBT_new $NBT_new){
		$this->value = $NBT_new->get($NBT_new->getShort());
	}

	public function write(NBT_new $NBT_new){
		$NBT_new->putShort(strlen($this->value));
		$NBT_new->put($this->value);
	}
}